for (var x=20; x<=30; x++) {
    if (x === 0) {
            document.write(x +  " is even <br>");
    }
    else if (x % 2 === 0) {
            document.write(x + " is even <br>");   
    }
    else {
            document.write(x + " is odd <br>");
    }
}


